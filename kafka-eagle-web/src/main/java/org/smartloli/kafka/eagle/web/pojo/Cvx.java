package org.smartloli.kafka.eagle.web.pojo;

public class Cvx {
    private Integer id;
    private String cvxcode;
    private String cvxDescription;
    private String helpCode;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getCvxcode() {
        return cvxcode;
    }

    public void setCvxcode(String cvxcode) {
        this.cvxcode = cvxcode;
    }

    public String getCvxDescription() {
        return cvxDescription;
    }

    public void setCvxDescription(String cvxDescription) {
        this.cvxDescription = cvxDescription;
    }

    public String getHelpCode() {
        return helpCode;
    }

    public void setHelpCode(String helpCode) {
        this.helpCode = helpCode;
    }
}
