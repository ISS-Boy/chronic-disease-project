package org.smartloli.kafka.eagle.grafana.JavaBean;
public class TimeRange {

    private String from;
    private String to;
    public void setFrom(String from) {
         this.from = from;
     }
     public String getFrom() {
         return from;
     }

    public void setTo(String to) {
         this.to = to;
     }
     public String getTo() {
         return to;
     }

}
